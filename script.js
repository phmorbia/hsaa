function openNav() {
    var sideNav = document.getElementById("mySidenav");
    sideNav.style.visibility = "visible";
    sideNav.style.width = "250px";
}

function closeNav() {
    var sideNav = document.getElementById("mySidenav");
    sideNav.style.visibility = "hidden";
    sideNav.style.width = "0";
}
